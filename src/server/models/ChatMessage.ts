import mongoose from "mongoose";

const chatMessageSchema = new mongoose.Schema(
  {
    content: {
      type: String,
      required: true,
    },
    user: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
  },
  {
    timestamps: true,
  },
);

export const ChatMessage = mongoose.model("ChatMessage", chatMessageSchema);
